// Calculate the average of 3 decimal numbers.

using System;
class AverageOfDecimalNumbers
{
    public static void Average()
    {
        int a = 10;
        int b = 20;
        int c = 30;
        int total = a + b + c;
        int average = total / 3;
        Console.WriteLine("The average is: " + average);
    }
}